#include <my.h>
#include <evalexpr.h>

int my_add(int num_1 , int num_2)
{
    return (num_1 + num_2);
}

int my_sub(int num_1 , int num_2)
{
    return (num_1 - num_2);
}

int my_div(int num_1 , int num_2)
{
    return (num_1 / num_2);
}

int my_mul(int num_1 , int num_2)
{
    return (num_1 * num_2);
}

int my_mod(int num_1 , int num_2)
{
    return (num_1 % num_2);
}
